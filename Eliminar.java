    public void ElimiEnTabla() {
        try {
            conectar();
            String comando = "DELETE FROM MARCADOR WHERE NUM_PAR_MAR=";
            st = cn.createStatement();
            rs = st.executeQuery(comando + jComboBox1.getSelectedItem().toString());
            commit();
            JOptionPane.showMessageDialog(null, "Dato Eliminado ");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error ingresando datos: " + ex);
//        cmdNuevoActionPerformed(null);
        }
    }

    public ArrayList<String> combot() {
        ArrayList<String> lista = new ArrayList<String>();
        String comando = "SELECT NUM_PAR_MAR FROM MARCADOR";
        try {
            conectar();
            st = cn.createStatement();
            rs = st.executeQuery(comando);
            while (rs.next()) {
                lista.add(rs.getString("NUM_PAR_MAR"));
            }

        } catch (SQLException | NumberFormatException | HeadlessException ex) {
            JOptionPane.showMessageDialog(null, "Error en la consulta \nError: " + ex);
        }
        return lista;
    }

    public void commit() {
        ResultSet rs = null;
        String sql = "COMMIT";
        try {
            conectar();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    public void conectar() {
        try {
            String url = "jdbc:oracle:thin:@localhost:1521:XE";
            cn = DriverManager.getConnection(url, "CAMPEONATO", "CAMPEONATO");
            st = cn.createStatement();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No ha sido posible conectarse \n");
        }
    }

    private void nuevoComboT() {
        combot();
        ArrayList<String> lista = new ArrayList<String>();
        lista = combot();
        for (int i = 0; i < lista.size(); i++) {
            jComboBox1.addItem(String.valueOf(lista.get(i)));
        }
    }

