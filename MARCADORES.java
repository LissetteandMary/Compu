    private void nuevoCombo() {
        combo();
        ArrayList<String> lis = new ArrayList<String>();
        lis = combo();
        for (int i = 0; i < lis.size(); i++) {
            cbxNP.addItem(String.valueOf(lis.get(i)));
        }
    }


    public void conectar() {
        try {
            String url = "jdbc:oracle:thin:@localhost:1521:XE";
            cn = DriverManager.getConnection(url, "CAMPEONATO", "CAMPEONATO");
            st = cn.createStatement();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No ha sido posible conectarse \n");
        }
    }

    public ArrayList<String> combo() {
        ArrayList<String> list = new ArrayList<String>();
        String comando = "SELECT NUM_PAR FROM PARTIDOS WHERE NUM_PAR NOT IN (SELECT NUM_PAR_MAR FROM MARCADOR)";
        try {
            conectar();
            st = cn.createStatement();
            rs = st.executeQuery(comando);
            while (rs.next()) {
                list.add(rs.getString("NUM_PAR"));
            }

        } catch (SQLException | NumberFormatException | HeadlessException ex) {
            JOptionPane.showMessageDialog(null, "Error en la consulta \nError: " + ex);
        }
        return list;
    }


    public void commit() {
        String sql = "COMMIT";
        try {
            conectar();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    public void ingresoEnTabla() {
        try {
            conectar();
            String result = "INSERT INTO MARCADOR VALUES(" + golL[contadorDeRegistros]
                    + "," + golV[contadorDeRegistros] + "," + num[contadorDeRegistros] + ")";
            st.execute(result);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error ingresando datos: " + ex);
//        cmdNuevoActionPerformed(null);
        }
    }

    public void ingresoDeDatos(int Num, int gl, int gv) {

        if (contadorDeRegistros < 24) {
            num[contadorDeRegistros] = Num;
            golL[contadorDeRegistros] = gl;
            golV[contadorDeRegistros] = gv;
            ingresoEnTabla();

        } else {
            JOptionPane.showMessageDialog(null, "Lo sentimos pero no puede ingresar mas datos.");
        }

    }

